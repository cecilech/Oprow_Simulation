package firstTry;

import java.sql.Time;
import java.util.List;

/**
 * Created by ELODE on 07/03/2017.
 */
public class Borne {
    private String borneNum;
    User userInProgress; // user who is using the borne
    private List<Time> timeNeed; // List contenant les temps nécessaire à la réalisations de la tache
    private Time timeRemaining; //temps restant avant la fin de l'exécution de la tache

    Borne(String aBorneNum, String aBorneUse, User anInProgress, List<Time> aTimeNeed){
        borneNum = aBorneNum;
        userInProgress = anInProgress;
        timeNeed = aTimeNeed;
    }


    //////////////////////////////////////////////////////////////////////////////////////////////
    //   setTimeRemaining
    //   permet de définir le contexte d'utilisation de la borne: QR=> borne qui ne récupère que les QRcode; noQR=> Borne qui permet l'inscription des User
    /////////////////////////////////////////////////////////////////////////////////////////////

    public void setTimeRemaining(Time timeRemaining) {
        this.timeRemaining = timeRemaining;
    }


    //////////////////////////////////////////////////////////////////////////////////////////////
    //   getTimeRemaining
    //   permet de récuperer le contexte d'utilisation de la borne: QR=> borne qui ne récupère que les QRcode; noQR=> Borne qui permet l'inscription des User
    /////////////////////////////////////////////////////////////////////////////////////////////


    public Time getTimeRemaining() {
        return timeRemaining;
    }

    //////////////////////////////////////////////////////////////////////////////////////////////
    //   setBornNum
    //   permet de définir le numéro de la borne
    /////////////////////////////////////////////////////////////////////////////////////////////
    public void setBorneNum(String pBornNb){
        borneNum = pBornNb;
    }

    //////////////////////////////////////////////////////////////////////////////////////////////
    //   getBornNum
    //   permet de récuperer le numéro de la borne
    /////////////////////////////////////////////////////////////////////////////////////////////
    public String getBornNum(){
        return borneNum;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////
    //   setTimeNeed
    //   permet de définir la liste timeNeed
    /////////////////////////////////////////////////////////////////////////////////////////////
    public void setTimeNeed(List<Time> pTimeNeed){
        timeNeed = pTimeNeed;
    }
    //////////////////////////////////////////////////////////////////////////////////////////////
    //   getTimeNeed
    //   permet de récuperer l numéro de la borne
    /////////////////////////////////////////////////////////////////////////////////////////////
    public List<Time> getTimeNeed(){
        return timeNeed;
    }
    //////////////////////////////////////////////////////////////////////////////////////////////
    //   isUsed
    //   retourne TRUE si la borne est en cours d'utilisation, retourne FALSE si la borne n'est pas utilisé
    /////////////////////////////////////////////////////////////////////////////////////////////
    public boolean isUsed(){
        return this.userInProgress != null;
    }
}
